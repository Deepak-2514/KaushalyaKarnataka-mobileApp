data class CategoryLabels(
    val all: String,
    val electrician: String,
    val plumber: String,
    val carpenter: String,
    val painter: String,
    val cleaner: String,
    val gardener: String,
    val other: String,
) {
    fun label(raw: String?): String = when (raw) {
        "All" -> all
        "Electrician" -> electrician
        "Plumber" -> plumber
        "Carpenter" -> carpenter
        "Painter" -> painter
        "Cleaner" -> cleaner
        "Gardener" -> gardener
        "Other" -> other
        else -> raw.orEmpty()
    }
}

